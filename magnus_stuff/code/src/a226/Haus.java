package a226;

public class Haus {
    private int zimmer;
    private float miete;

    public Haus(){
    }

    public int getZimmer() {
        return zimmer;
    }

    public void setZimmer(int anzahl) {
        this.zimmer = anzahl;
    }

    public float getMiete() {
        return miete;
    }

    public void setMiete(float betrag) {
        this.miete = betrag;
    }
}
